package it.controllers;

public class ControllerA {

	public ControllerA() {
		super();
	}

	private String nome;

	private String congome;

	private String descrizione;

	private String piva;

	private String azienda;

	private String codAzienda;

	/**
	 * @return the nome
	 */
	public String getNome() {
		return nome;
	}

	/**
	 * @param nome the nome to set
	 */
	public void setNome(String nome) {
		this.nome = nome;
	}

	/**
	 * @return the congome
	 */
	public String getCongome() {
		return congome;
	}

	/**
	 * @param congome the congome to set
	 */
	public void setCongome(String congome) {
		this.congome = congome;
	}

	/**
	 * @return the descrizione
	 */
	public String getDescrizione() {
		return descrizione;
	}

	/**
	 * @param descrizione the descrizione to set
	 */
	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}

	/**
	 * @return the piva
	 */
	public String getPiva() {
		return piva;
	}

	/**
	 * @param piva the piva to set
	 */
	public void setPiva(String piva) {
		this.piva = piva;
	}

	/**
	 * @return the azienda
	 */
	public String getAzienda() {
		return azienda;
	}

	/**
	 * @param azienda the azienda to set
	 */
	public void setAzienda(String azienda) {
		this.azienda = azienda;
	}

	/**
	 * @return the codAzienda
	 */
	public String getCodAzienda() {
		return codAzienda;
	}

	/**
	 * @param codAzienda the codAzienda to set
	 */
	public void setCodAzienda(String codAzienda) {
		this.codAzienda = codAzienda;
	}

}
